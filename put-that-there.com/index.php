<p>
	<a name="davidbio">*</a> David Reinfurt is an independent graphic designer, writer and editor based in New York City. In 2000, David founded O-R-G, a flexible graphic design practice composed of a constantly shifting network of collaborators. In 2006, together with graphic designer Stuart Bailey David established Dexter Sinister, a "just-in-time workshop and occasional bookstore" that explored alternate distribution strategies by collapsing distinctions of editing, design, production and  distribution into one efficient activity. Dexter Sinister published the semi-annual arts magazine Dot Dot Dot from 2006-2011. In 2011, David co-founded The Serving Library with Stuart Bailey and Angie Keefer, a cooperatively-built archive that assembles itself by publishing. It consists of "1. an ambitious public website; 2. a small physical library space; 3. a publishing program which runs through #1 and #2."
