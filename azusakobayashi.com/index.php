            <div class="image"><img src="img/azu_lvd.gif"></div>
            <div class="caption"><em>A Catalog of Publications in Space, Supposed and Recalled</em>, book, 2012<br />
                A speculative catalog for the seminal publications in the history of design and architecture. It consists of a series of staged photographs featuring an empty bookstand in varying constructed sets that evoke the different time periods, and in places in between. The publications are represented by loose postcards tucked into the catalog, and can be used as bookmarks, inserted wherever the reader thinks is appropriate. The postcards are not bound to the catalog—literally or figuratively—and they will most likely be lost as the catalog is handled by multiple readers.<br />
                History is presented here as a gradual transition—with the occasional repetition, skipping, and false starts. The publications pass through this portal of time, to be lost, remembered, cherished.
            </div>
